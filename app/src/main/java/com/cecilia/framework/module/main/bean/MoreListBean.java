package com.cecilia.framework.module.main.bean;

public class MoreListBean {

        private int id;
        private String name;
        private int pid;
        private int level;
        private String image;
        public void setId(int id) {
            this.id = id;
        }
        public int getId() {
            return id;
        }

        public void setName(String name) {
            this.name = name;
        }
        public String getName() {
            return name;
        }

        public void setPid(int pid) {
            this.pid = pid;
        }
        public int getPid() {
            return pid;
        }

        public void setLevel(int level) {
            this.level = level;
        }
        public int getLevel() {
            return level;
        }

        public void setImage(String image) {
            this.image = image;
        }
        public String getImage() {
            return image;
        }
}
